import turtle
t   = turtle.Turtle()
t.pensize(3)
t.color("teal")
def chu_nhat(dai,rong):
    t.fd(dai)
    t.right(90)
    t.fd(rong)
    t.right(90)
    t.fd(dai)
    t.right(90)
    t.fd(rong)
    t.right(90)

def tam_giac(canh,goc):
    for i in range(3):
        t.fd(canh)
        t.right(goc)
        t.fd(canh)
